 /* 원형 그래프*/



$(function(){
  var perNum = 90;
$('.photoshop .second.circle').circleProgress({
	  value: perNum/100,
	startAngle:300,
	size:400,
	fill:{
	  gradient:["#fff","#fff"],
	gradientAngle: Math.PI / 2
	},
	animation:{
	  duration:2200,
	easing:"swing"
	},
	lineCap : "butt",
	reverse:true

	}).on('circle-animation-progress', function(event, progress) {
	$(this).find('strong').html(Math.round(perNum * progress) + '<i>%</i>');
	});
});




